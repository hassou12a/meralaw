async function main() {
  console.log(`➕ Adding ${newLaws.length} new REAL Algerian laws...\n`);

  let added = 0;
  let skipped = 0;

  for (const law of newLaws) {
    try {
      const created = await prisma.law.upsert({
        where: { referenceNumber: law.referenceNumber },
        update: law,
        create: law,
      });
      console.log(`✅ ${added + 1}. [${created.referenceNumber}] ${created.titleAr}`);
      console.log(`   PDF: ${created.pdfUrlAr}`);
      added++;
    } catch (err) {
      console.log(`⚠️  Skipped ${law.referenceNumber}: ${err.message}`);
      skipped++;
    }
  }

  const total = await prisma.law.count();
  console.log(`\n📊 Results: ${added} added, ${skipped} skipped`);
  console.log(`📊 Total laws in database: ${total}`);
  console.log('\n🎉 Done!');
  await prisma.$disconnect();
}

main().catch(e => { console.error(e); process.exit(1); });
